package Introduction;

public class MultiplyTwoFloatingPointNumbers {
    
    /* This program will multiply two floating point numbers, store the
    result and display it on screen
     */

     public static void main(String[] args) {
         float firstNum = 1.5f;
         float secondNum = 2.0f;

         float product = firstNum * secondNum;

         System.out.println(product);
     }
}
